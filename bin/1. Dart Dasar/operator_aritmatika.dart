void main() {
  // Belajar Operator Aritmatika
  var angka1 = 10;
  var angka2 = 3;

  var hasilTambah = angka1 + angka2;
  var hasilKurang = angka1 - angka2;
  var hasilNegatif = -angka1;
  var hasilKali = angka1 * angka2;
  var hasilBagi = angka1 / angka2;
  var hasilBagiInt = angka1 ~/ angka2;
  var hasilModulo = angka1 % angka2;

  print(hasilTambah);
  print(hasilKurang);
  print(hasilNegatif);
  print(hasilKali);
  print(hasilBagi);
  print(hasilBagiInt);
  print(hasilModulo);
}
